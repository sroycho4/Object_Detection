    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File("DataSet\\"));

        int status = fileChooser.showOpenDialog(null);
        if (status == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            filename1 = selectedFile.getPath();
            jLabel1.setIcon(new ImageIcon("" + filename1));
            try {
                BufferedImage i = ImageIO.read(selectedFile);
                ImageIO.write(i, "PNG", new File("Input\\Object.png"));
            } catch (Exception ex) {
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed
